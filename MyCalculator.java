package week1.day2;

public class MyCalculator {

	public static void main(String[] args) {
		
		Calculator calc = new Calculator();
		calc.addNumber(28, 11, 22);
		System.out.println("Sum of Numbers: "+calc.sum);
		calc.subNumber(28, 22);
		System.out.println("Difference of Numbers: "+calc.diff);
		calc.mulNumber(28, 11);
		System.out.println("Product of Numbers: "+calc.product);
		calc.divNumber(22, 28);
		System.out.println("Quotient of Numbers: "+calc.quotient);
		calc.reminder(28, 11);
		System.out.println("Reminder of Numbers: "+calc.reminder);
		
	}

}
